.ORIG x3000

; R1	x5000 0
; R2	x5001 1
; R3	x5002 
; R4	TEMP
; R5	Current Line
; R6	Inner Loop register

; Initialize
LD R3, LOCATION	;
ADD R1, R3, #0	;
ADD R2, R3, #1	;
LDR R3, R3, #2	;

; Calculate the address of the first line
LD R4, CONST_4	;
LOOP_MUL	ADD R3, R3, R3	;
					ADD R4, R4, #-1	;
					BRp LOOP_MUL	;
LEA R4, FONT_DATA	;
ADD R3, R3, R4	;

					LD R4, CONST_16	;
LOOP_OUT	LDR R5, R3, #0	;
					LD R6, CONST_8	;
	LOOP_INER	AND R5, R5, #-1	;
						BRn NEGA	;
						LDR R0, R1, #0	;
						JSR #1	;
	NEGA			LDR R0, R2, #0	;
						TRAP x21	;
						ADD R5, R5, R5	;
						ADD R6, R6, #-1	;
						BRp LOOP_INER	;
					; New Line 
					LD R0, ENTER	;
					TRAP x21	;
					ADD R3, R3, #1	;
					ADD R4, R4, #-1	;
					BRp LOOP_OUT	;

TRAP x25	;

