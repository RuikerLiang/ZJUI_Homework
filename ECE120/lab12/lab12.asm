; This program is to print a charater to the console.
; The charater will be printed is stored in x5002.
; The chatater will be printed according to the font data.
; In the font data, the place of 0 will be replaced with the chatater in x5000 and 1 with charater in x5001.
.ORIG x3000

; R0	Character to Be Printed
; R3	x5002
;			Line pointer
; R4	Loop Counter in Multiplication
;			Loop Counter of Line in Output
; R5	Line to Be Output
; R6	Loop Counter of Column in Output

; Initialize
LD R3, LOCATION_5000	;
LDR R3, R3, #2	;

; Calculate the address of the first line
; Store the address of the first line in R3
; R3 <- R3 * 16 + FONT_DATA
LD R4, CONST_4	;
LOOP_MUL
	ADD R3, R3, R3	;
	ADD R4, R4, #-1	;
	BRp LOOP_MUL	;
LEA R4, FONT_DATA	;
ADD R3, R3, R4	;

; Main Loop to Output
LD R4, CONST_16	;
;	Loop of the Line
LOOP_OUT
	LDR R5, R3, #0	;
	LD R6, CONST_8	;
	; Loop of the Column
	LOOP_INER
		; Check if the most significant number is 1 or 0 
		LDI R0, LOCATION_5001	;
		AND R5, R5, #-1	; To set cc
		BRn NEGA	;
		LDI R0, LOCATION_5000	;
		NEGA
		TRAP x21	;
		ADD R5, R5, R5	; Current line shift left one bit to move to next charater 
		ADD R6, R6, #-1	;
		BRp LOOP_INER	;
	; Start A New Line 
	LD R0, ENTER	;
	TRAP x21	;
	ADD R3, R3, #1	; Change to next line
	ADD R4, R4, #-1	;
	BRp LOOP_OUT	;

TRAP x25	; Halt

CONST_16 .FILL x0010	;
CONST_8	.FILL x0008	;
CONST_4	.FILL x0004	;
ENTER .FILL x000A	;
LOCATION_5000 .FILL x5000	;
LOCATION_5001 .FILL x5001	;

